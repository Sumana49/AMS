package com.avnet.ams.vto;


public class ViewRequestsVTO {

	public String employeeID;
	public String type;
	public String name;
	public String status;
	public String designation;
	public String requestID;
	public String purpose;
	public String severity;
	public String dateOfApproval;
	public String dateOfIssue;
	public String dueDate;
	public String comments;
	public String categoryId;
	

	
	

	

	public ViewRequestsVTO() {
		super();
	}

	public ViewRequestsVTO(String employeeID, String type, String name,
			String status, String designation, String requestID,
			String purpose, String severity, String dateOfApproval,
			String dateOfIssue, String dueDate,String comments,String categoryId) {
		super();
		this.employeeID = employeeID;
		this.type = type;
		this.name = name;
		this.status = status;
		this.designation = designation;
		this.requestID = requestID;
		this.purpose = purpose;
		this.severity = severity;
		this.dateOfApproval = dateOfApproval;
		this.dateOfIssue = dateOfIssue;
		this.dueDate = dueDate;
		this.comments=comments;
		this.categoryId=categoryId;
	}
	public String getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public final String getEmployeeID() {
		return employeeID;
	}


	public final void setEmployeeID(String employeeID) {
		this.employeeID = employeeID;
	}


	public final String getType() {
		return type;
	}


	public final void setType(String type) {
		this.type = type;
	}


	public final String getName() {
		return name;
	}


	public final void setName(String name) {
		this.name = name;
	}


	public final String getStatus() {
		return status;
	}


	public final void setStatus(String status) {
		this.status = status;
	}


	public final String getDesignation() {
		return designation;
	}


	public final void setDesignation(String designation) {
		this.designation = designation;
	}


	public final String getRequestID() {
		return requestID;
	}


	public final void setRequestID(String requestID) {
		this.requestID = requestID;
	}


	public final String getPurpose() {
		return purpose;
	}


	public final void setPurpose(String purpose) {
		this.purpose = purpose;
	}


	public final String getSeverity() {
		return severity;
	}


	public final void setSeverity(String severity) {
		this.severity = severity;
	}


	public final String getDateOfApproval() {
		return dateOfApproval;
	}


	public final void setDateOfApproval(String dateOfApproval) {
		this.dateOfApproval = dateOfApproval;
	}


	public final String getDateOfIssue() {
		return dateOfIssue;
	}


	public final void setDateOfIssue(String dateOfIssue) {
		this.dateOfIssue = dateOfIssue;
	}


	public final String getDueDate() {
		return dueDate;
	}


	public final void setDueDate(String dueDate) {
		this.dueDate = dueDate;
	}

	

}
